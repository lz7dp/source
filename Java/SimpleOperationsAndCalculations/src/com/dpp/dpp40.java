package com.dpp;

public class dpp40 {
    public static void main(String[] args) {
        int a = 1;
        System.out.println(a+1);
        System.out.println(++a);
        System.out.println(a++);
        System.out.println(a);
        System.out.println(a+=1);
        System.out.println(a+=a);

        System.out.println(a-1);
        System.out.println(--a);
        System.out.println(a--);
        System.out.println(a);
        System.out.println(a-=1);
        System.out.println(a-=a);

    }
}
